--   Ctrl-Shift-n    选中所有匹配
--   Ctrl-j          向下添加光标
--   Ctrl-Up         向上添加光标
--
-- 多光标模式内：
--   q               跳过当前匹配，选下一个
--   Q               跳过当前匹配，选上一个
--   Esc             退出多光标（清除所有额外光标）
--
-- 编辑（和普通 vim 一样，所有光标同时执行）：
--   c / d / x       修改 / 删除
--   I / A           Visual选区每行头/尾插入
--   i / a           进入插入模式
--   <leader>ma      对齐所有光标列
